#include "PrecompiledHeaders.h"
#include "Time/Delay.h"





//-----------------------------------------------------------------------------------------------------------------------------------------
//Class Delay
//-----------------------------------------------------------------------------------------------------------------------------------------
Delay::Delay(float coolDown)
	:mCoolDown(coolDown), mTimePassed(0.0f)
{

}



void Delay::start()
{
	mTimePassed = 0.0f;
}



void Delay::update(float deltaTime)
{
	mTimePassed += deltaTime;
}



bool Delay::isEnd()
{
	return mTimePassed >= mCoolDown;
}
