SignatureManager::SignatureManager()
{
	for (unsigned int e = 0; e < MAX_ENTITIES; e++)
	{
		for (unsigned int i = 0; i < MAX_TYPE_CMPS; i++)
		{
			signatures[(Entity)e][i] = 0;
		}
	}
}
